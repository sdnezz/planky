package com.example.plango

import java.time.LocalDateTime

fun adviceDebug(run: PriorityRun): String {
    return run.items
        .sortedBy { it.queuePosition }
        .joinToString(separator = "\n\n") { item ->
            buildString {
                append("${item.queuePosition}. ${item.title}: ")
                append(eAdvice(item.e))
                append(" ")
                append(dAdvice(item.d, item.deadline))
                append(" ")
                append(cAdvice(item.c, item.productivityPeriod))
                append(" ")
                append(rAdvice(item.r, item.deadline != null))
            }
        }
}

private fun eAdvice(score: Double): String {
    return when (score) {
        1.0 -> "Важные и срочные задачи самые приоритетные к выполнению, так как они являются обязанностями или ключевыми действиями, которые необходимо выполнить как можно скорее."
        0.75 -> "Важные, но не срочные задачи являются стратегически значимыми планами для долгосрочного результата, поэтому подобные задачи следует выполнять после обязательных важных и срочных."
        0.5 -> "Не важные, но срочные задачи занимают третье место по приоритету, так как зачастую являются незначимыми для личных целей планами, которые следует автоматизировать, выполнить быстро или отложить."
        0.25 -> "Не важные и не срочные задачи зачастую не приносят пользы, поэтому следует выполнять их в последнюю очередь."
        else -> "Матрица Эйзенхауэра не дала ожидаемое значение, поэтому задача получила нейтральную интерпретацию."
    }
}

private fun dAdvice(score: Double, deadline: LocalDateTime?): String {
    if (deadline == null) {
        return "Срока для выполнения нет, поэтому задача менее приоритетна по сравнению с ограниченными по времени задачами."
    }

    return when {
        score >= 0.85 -> "Дедлайн создаёт очень сильное давление, поэтому задачу нужно брать практически сразу."
        score >= 0.65 -> "Срок выполнения уже заметно влияет на приоритет и делает задачу достаточно срочной."
        score >= 0.35 -> "Дедлайн влияет умеренно: задача ещё не критична, но откладывать её надолго не стоит."
        else -> "Дедлайн далёк, поэтому его влияние на приоритет остаётся слабым."
    }
}

private fun cAdvice(score: Double, productivityPeriod: String): String {
    return when (productivityPeriod) {
        "peak" -> when {
            score >= 0.75 -> "Сейчас у вас пик продуктивности, и эта задача хорошо совпадает с наиболее сильным рабочим ресурсом."
            score >= 0.45 -> "Сейчас пик продуктивности, но задача лишь частично соответствует этому окну, поэтому её можно делать, хотя есть и более удачные кандидаты."
            else -> "Сейчас пик продуктивности, но задача слишком проста или слишком невыгодна для этого окна."
        }

        "normal" -> when {
            score >= 0.75 -> "Сейчас обычный рабочий период, и задача хорошо укладывается в стабильный темп."
            score >= 0.45 -> "Сейчас нормальный режим, поэтому задача выполнима без лишней перегрузки."
            else -> "Сейчас обычный режим, но эта задача слабо соответствует текущему рабочему окну."
        }

        else -> when {
            score >= 0.75 -> "Сейчас спад продуктивности, поэтому лучше брать простые задачи; эта задача подходит для такого состояния."
            score >= 0.45 -> "Сейчас спад продуктивности, и задача всё ещё допустима как более лёгкая работа."
            else -> "Сейчас спад продуктивности, а задача слишком тяжёлая для такого периода."
        }
    }
}

private fun rAdvice(score: Double, hasDeadline: Boolean): String {
    if (!hasDeadline) {
        return "Срок выполнения не задан, поэтому риск исполнения определяется почти только сложностью задачи и остаётся ниже, чем у задач с жёстким дедлайном."
    }

    return when {
        score >= 0.75 -> "Риск выполнения высокий: сложность и срок одновременно создают сильное давление, поэтому задачу лучше не откладывать."
        score >= 0.45 -> "Риск заметный: задача требует внимания, иначе возрастает шанс не успеть."
        score >= 0.20 -> "Риск умеренный: задача не выглядит критичной, но терять её из виду не стоит."
        else -> "Риск минимален: даже при наличии срока задача не создаёт сильного напряжения."
    }
}